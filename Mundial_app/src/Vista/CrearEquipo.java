    private void btn_agregarequipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarequipoActionPerformed
        String nombre = txt_nombre.getText();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre", "Validacion", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        
        String confederacion = cbo_confederacion.getSelectedItem().toString();
        if (confederacion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre", "Validacion", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        

        int cantidadTitulos;
        try {
            cantidadTitulos = Integer.parseInt(txt_titulosmundiales.getText());

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Debe ingresar un precio valido", "Validacion", JOptionPane.WARNING_MESSAGE);
            return;

        }
        
        int posicionFifa;
        try {
            posicionFifa = Integer.parseInt(txt_posicionfifa.getText());

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Debe ingresar un precio valido", "Validacion", JOptionPane.WARNING_MESSAGE);
            return;

        }
        
        
        
         boolean debutante = chk_debutante.isSelected();
         
        
        
        

        
        Equipo nuevoEquipo = new Equipo();
        
        nuevoEquipo.setNombre(nombre);
        nuevoEquipo.setConfederacion(confederacion);
        nuevoEquipo.setCantidadTitulos(cantidadTitulos);
        nuevoEquipo.setPosicionFifa(posicionFifa);
        nuevoEquipo.setDebutante(debutante);

        ControladorEquipo cl = new ControladorEquipo();
        if (cl.agregarEquipo(nuevoEquipo)) {

            JOptionPane.showConfirmDialog(this, "Producto creado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error", "Validacion", JOptionPane.WARNING_MESSAGE);
        }        
    }//GEN-LAST:event_btn_agregarequipoActionPerformed

